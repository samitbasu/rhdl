pub mod vlog {
    include!("../src/ast.rs");
}
fn main() {
    let _ = vlog::module_list({
        let mut ret = Vec::with_capacity(1usize);
        ret.push(
            vlog::module_def(
                stringify!(literal_formats),
                {
                    let mut ret = Vec::with_capacity(2usize);
                    ret.push(
                        vlog::port(
                            vlog::input(),
                            vlog::declaration(
                                vlog::wire(),
                                vlog::unsigned(0..=7),
                                stringify!(data),
                            ),
                        ),
                    );
                    ret.push(
                        vlog::port(
                            vlog::output(),
                            vlog::declaration(
                                vlog::reg(),
                                vlog::unsigned(0..=31),
                                stringify!(result),
                            ),
                        ),
                    );
                    ret
                },
                {
                    let mut ret = Vec::with_capacity(5usize);
                    ret.push(
                        vlog::stmt_item(
                            vlog::local_param_stmt(
                                stringify!(DECIMAL_VAL),
                                vlog::const_int(42),
                            ),
                        ),
                    );
                    ret.push(
                        vlog::stmt_item(
                            vlog::local_param_stmt(
                                stringify!(HEX_VAL),
                                vlog::const_verilog(
                                    vlog::lit_verilog(8, stringify!(hA5).into()),
                                ),
                            ),
                        ),
                    );
                    ret.push(
                        vlog::stmt_item(
                            vlog::local_param_stmt(
                                stringify!(BIN_VAL),
                                vlog::const_verilog(
                                    vlog::lit_verilog(8, stringify!(b1010_0101).into()),
                                ),
                            ),
                        ),
                    );
                    ret.push(
                        vlog::stmt_item(
                            vlog::local_param_stmt(
                                stringify!(OCT_VAL),
                                vlog::const_verilog(
                                    vlog::lit_verilog(8, stringify!(o245).into()),
                                ),
                            ),
                        ),
                    );
                    ret.push(
                        vlog::stmt_item(
                            vlog::always_stmt(
                                {
                                    let mut ret = Vec::with_capacity(1usize);
                                    ret.push(vlog::star());
                                    ret
                                },
                                vlog::block_stmt({
                                    let mut ret = Vec::with_capacity(4usize);
                                    ret.push(
                                        vlog::assign_stmt(
                                            vlog::assign_target_index(
                                                vlog::index_expr(
                                                    stringify!(result),
                                                    vlog::literal_expr(7),
                                                    Some(vlog::literal_expr(0)),
                                                ),
                                            ),
                                            vlog::binary_expr(
                                                vlog::ident_expr(stringify!(data)),
                                                vlog::binary_plus(),
                                                vlog::ident_expr(stringify!(DECIMAL_VAL)),
                                            ),
                                        ),
                                    );
                                    ret.push(
                                        vlog::assign_stmt(
                                            vlog::assign_target_index(
                                                vlog::index_expr(
                                                    stringify!(result),
                                                    vlog::literal_expr(15),
                                                    Some(vlog::literal_expr(8)),
                                                ),
                                            ),
                                            vlog::binary_expr(
                                                vlog::ident_expr(stringify!(data)),
                                                vlog::binary_xor(),
                                                vlog::ident_expr(stringify!(HEX_VAL)),
                                            ),
                                        ),
                                    );
                                    ret.push(
                                        vlog::assign_stmt(
                                            vlog::assign_target_index(
                                                vlog::index_expr(
                                                    stringify!(result),
                                                    vlog::literal_expr(23),
                                                    Some(vlog::literal_expr(16)),
                                                ),
                                            ),
                                            vlog::binary_expr(
                                                vlog::ident_expr(stringify!(data)),
                                                vlog::binary_and(),
                                                vlog::ident_expr(stringify!(BIN_VAL)),
                                            ),
                                        ),
                                    );
                                    ret.push(
                                        vlog::assign_stmt(
                                            vlog::assign_target_index(
                                                vlog::index_expr(
                                                    stringify!(result),
                                                    vlog::literal_expr(31),
                                                    Some(vlog::literal_expr(24)),
                                                ),
                                            ),
                                            vlog::binary_expr(
                                                vlog::ident_expr(stringify!(data)),
                                                vlog::binary_or(),
                                                vlog::ident_expr(stringify!(OCT_VAL)),
                                            ),
                                        ),
                                    );
                                    ret
                                }),
                            ),
                        ),
                    );
                    ret
                },
            ),
        );
        ret
    });
}
