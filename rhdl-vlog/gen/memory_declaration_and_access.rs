pub mod vlog {
    include!("../src/ast.rs");
}
fn main() {
    let _ = vlog::module_list({
        let mut ret = Vec::with_capacity(1usize);
        ret.push(
            vlog::module_def(
                stringify!(memory_test),
                {
                    let mut ret = Vec::with_capacity(5usize);
                    ret.push(
                        vlog::port(
                            vlog::input(),
                            vlog::declaration(
                                vlog::wire(),
                                vlog::unsigned(0..=0),
                                stringify!(clk),
                            ),
                        ),
                    );
                    ret.push(
                        vlog::port(
                            vlog::input(),
                            vlog::declaration(
                                vlog::wire(),
                                vlog::unsigned(0..=2),
                                stringify!(addr),
                            ),
                        ),
                    );
                    ret.push(
                        vlog::port(
                            vlog::input(),
                            vlog::declaration(
                                vlog::wire(),
                                vlog::unsigned(0..=7),
                                stringify!(data_in),
                            ),
                        ),
                    );
                    ret.push(
                        vlog::port(
                            vlog::input(),
                            vlog::declaration(
                                vlog::wire(),
                                vlog::unsigned(0..=0),
                                stringify!(we),
                            ),
                        ),
                    );
                    ret.push(
                        vlog::port(
                            vlog::output(),
                            vlog::declaration(
                                vlog::reg(),
                                vlog::unsigned(0..=7),
                                stringify!(data_out),
                            ),
                        ),
                    );
                    ret
                },
                {
                    let mut ret = Vec::with_capacity(2usize);
                    ret.push(
                        vlog::declaration_item(
                            vlog::declaration_list(
                                vlog::reg(),
                                vlog::unsigned(0..=7),
                                {
                                    let mut ret = Vec::with_capacity(1usize);
                                    ret.push(
                                        vlog::decl_kind(
                                            stringify!(memory),
                                            Some(vlog::unsigned(7..=0)),
                                        ),
                                    );
                                    ret
                                },
                            ),
                        ),
                    );
                    ret.push(
                        vlog::stmt_item(
                            vlog::always_stmt(
                                {
                                    let mut ret = Vec::with_capacity(1usize);
                                    ret.push(vlog::pos_edge(stringify!(clk)));
                                    ret
                                },
                                vlog::block_stmt({
                                    let mut ret = Vec::with_capacity(2usize);
                                    ret.push(
                                        vlog::if_stmt(
                                            vlog::ident_expr(stringify!(we)),
                                            vlog::block_stmt({
                                                let mut ret = Vec::with_capacity(1usize);
                                                ret.push(
                                                    vlog::nonblock_assign_stmt(
                                                        vlog::assign_target_index(
                                                            vlog::index_expr(
                                                                stringify!(memory),
                                                                vlog::ident_expr(stringify!(addr)),
                                                                None,
                                                            ),
                                                        ),
                                                        vlog::ident_expr(stringify!(data_in)),
                                                    ),
                                                );
                                                ret
                                            }),
                                            None,
                                        ),
                                    );
                                    ret.push(
                                        vlog::nonblock_assign_stmt(
                                            vlog::assign_target_ident(stringify!(data_out)),
                                            vlog::index_expr(
                                                stringify!(memory),
                                                vlog::ident_expr(stringify!(addr)),
                                                None,
                                            ),
                                        ),
                                    );
                                    ret
                                }),
                            ),
                        ),
                    );
                    ret
                },
            ),
        );
        ret
    });
}
